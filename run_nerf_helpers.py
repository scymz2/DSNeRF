class NeRF(nn.Module):
    def __init__(self, D=8, W=256, input_ch=3, input_ch_views=3, output_ch=4, skips=[4], use_viewdirs=False):
        """
        初始化 NeRF 网络。
        参数:
        - D: 网络的深度，即网络中线性层的数量。
        - W: 每个线性层的宽度，即神经元数或特征维数。
        - input_ch: 输入点的通道数。
        - input_ch_views: 视角依赖的输入通道数。
        - output_ch: 输出通道数。
        - skips: 跳连层的索引列表。
        - use_viewdirs: 是否使用视角方向作为额外的输入。
        """
        super(NeRF, self).__init__()
        self.D = D
        self.W = W
        self.input_ch = input_ch
        self.input_ch_views = input_ch_views
        self.skips = skips
        self.use_viewdirs = use_viewdirs
        
        # 点特征变换层
        self.pts_linears = nn.ModuleList(
            [nn.Linear(input_ch, W)] + [nn.Linear(W, W) if i not in self.skips else nn.Linear(W + input_ch, W) for i in range(D-1)])
        
        ### Implementation according to the official code release (https://github.com/bmild/nerf/blob/master/run_nerf_helpers.py#L104-L105)
        # 视角方向的处理层，根据官方代码只使用一层变换
        self.views_linears = nn.ModuleList([nn.Linear(input_ch_views + W, W//2)])

        ### Implementation according to the paper
        # self.views_linears = nn.ModuleList(
        #     [nn.Linear(input_ch_views + W, W//2)] + [nn.Linear(W//2, W//2) for i in range(D//2)])
        
        if use_viewdirs:
            # 如果使用视角方向，分别定义用于RGB和透明度(alpha)的线性层
            self.feature_linear = nn.Linear(W, W)
            self.alpha_linear = nn.Linear(W, 1)
            self.rgb_linear = nn.Linear(W//2, 3)
        else:
            # 不使用视角方向时，直接输出预定通道数
            self.output_linear = nn.Linear(W, output_ch)

    def forward(self, x):
        # 将输入数据分为位置和视角两部分
        # split 相当于直接将输入数据 x 按照指定的维度分割为两部分 (x, y, z) 和 （θ, φ）
        # xyz是三维的，θφ实际上也是三维的，只是在这里用两个数表示（是x'y'z'）
        input_pts, input_views = torch.split(x, [self.input_ch, self.input_ch_views], dim=-1)
        h = input_pts
        
        # 通过点特征层处理位置数据
        for i, l in enumerate(self.pts_linears):
            h = self.pts_linears[i](h)
            h = F.relu(h)
            if i in self.skips:
                # 如果层在跳连列表中，将输入点数据与当前层输出连接
                h = torch.cat([input_pts, h], -1)

        # 处理视角方向数据
        if self.use_viewdirs:
            alpha = self.alpha_linear(h) # 计算透明度
            feature = self.feature_linear(h) # 提取特征
            h = torch.cat([feature, input_views], -1) # 将视角数据27维与特征连接
        
            for i, l in enumerate(self.views_linears):
                h = self.views_linears[i](h)
                h = F.relu(h)

            rgb = self.rgb_linear(h) # 计算RGB颜色
            outputs = torch.cat([rgb, alpha], -1) # 将颜色和透明度合并为最终输出
        else:
            # 不使用视角方向，直接从位置特征输出结果
            outputs = self.output_linear(h)

        return outputs    

    def load_weights_from_keras(self, weights):
        assert self.use_viewdirs, "Not implemented if use_viewdirs=False"
        
        # Load pts_linears
        for i in range(self.D):
            idx_pts_linears = 2 * i
            self.pts_linears[i].weight.data = torch.from_numpy(np.transpose(weights[idx_pts_linears]))    
            self.pts_linears[i].bias.data = torch.from_numpy(np.transpose(weights[idx_pts_linears+1]))
        
        # Load feature_linear
        idx_feature_linear = 2 * self.D
        self.feature_linear.weight.data = torch.from_numpy(np.transpose(weights[idx_feature_linear]))
        self.feature_linear.bias.data = torch.from_numpy(np.transpose(weights[idx_feature_linear+1]))

        # Load views_linears
        idx_views_linears = 2 * self.D + 2
        self.views_linears[0].weight.data = torch.from_numpy(np.transpose(weights[idx_views_linears]))
        self.views_linears[0].bias.data = torch.from_numpy(np.transpose(weights[idx_views_linears+1]))

        # Load rgb_linear
        idx_rbg_linear = 2 * self.D + 4
        self.rgb_linear.weight.data = torch.from_numpy(np.transpose(weights[idx_rbg_linear]))
        self.rgb_linear.bias.data = torch.from_numpy(np.transpose(weights[idx_rbg_linear+1]))

        # Load alpha_linear
        idx_alpha_linear = 2 * self.D + 6
        self.alpha_linear.weight.data = torch.from_numpy(np.transpose(weights[idx_alpha_linear]))
        self.alpha_linear.bias.data = torch.from_numpy(np.transpose(weights[idx_alpha_linear+1]))

